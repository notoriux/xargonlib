package it.xargon.xmp;

public class XmpRemoteException extends XmpRuntimeException {
   public XmpRemoteException() {super();}
   public XmpRemoteException(String message) {super(message);}
   public XmpRemoteException(String message, Throwable cause) {super(message,cause);}
   public XmpRemoteException(Throwable cause) {super(cause);}
}
