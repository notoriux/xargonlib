package it.xargon.xrpc;

public class XRpcNullPointerException extends XRpcException {
   public XRpcNullPointerException() {super();}
   public XRpcNullPointerException(String message) {super(message);}
   public XRpcNullPointerException(Throwable cause) {super(cause);}
   public XRpcNullPointerException(String message, Throwable cause) {super(message, cause);}
}
