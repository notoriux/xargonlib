package it.xargon.rxmp;

public class XmpRuntimeException extends RuntimeException {
   public XmpRuntimeException() {super();}
   public XmpRuntimeException(String message) {super(message);}
   public XmpRuntimeException(String message, Throwable cause) {super(message,cause);}
   public XmpRuntimeException(Throwable cause) {super(cause);}
}
